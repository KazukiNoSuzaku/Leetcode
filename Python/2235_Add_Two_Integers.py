# Problem: 2235. Add Two Integers
# URL: https://leetcode.com/problems/add-two-integers/
# Difficulty: Easy
#
# Approach:
# Simply return the sum of the two given integers.

class Solution(object):
    def sum(self, num1, num2):
        """
        :type num1: int
        :type num2: int
        :rtype: int
        """
        return num1 + num2
